package fundamentos;

public class VariaveisEConstants {
    static void main() {

        // 1. Declarar um variável
        int idade;

        // 2. Inicializar a variável
        idade = 3;

        // Constante -> final
        final String nome = "Vitor";
        // nome = "João Pereira"; -> erro

        int a = 4;
        int b = 2;
        int resultado = a + b;

        System.out.println(nome);
        System.out.println(idade);
        System.out.println(resultado);
    }
}
